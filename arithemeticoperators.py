# floor division - that gives the result of the division as an integer
# order of execution of arithmetic operators :
# B - brackets
# E - exponent
# D - division
# M - multiplication
# A - addition
# S - subtraction

a = 1
b = 2
print("addition", a + b)
print("subtraction", a - b)
print("multiplication", a * b)
print("division", a / b)
print("modulo", a % b)
print("exponent", a**b)
print("floor division", a // b)

